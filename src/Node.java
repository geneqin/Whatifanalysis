
public class Node {
	private int id;
	private int qlength;
	public Node(int i, int q){
		this.id=i;
		this.qlength=q;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getQlength() {
		return qlength;
	}
	public void setQlength(int qlength) {
		this.qlength = qlength;
	}

}
